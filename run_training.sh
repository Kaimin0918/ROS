#!/bin/bash

# 設定一個無限迴圈，確保訓練可以不斷重啟
while true; do
    echo "=========================================="
    echo "啟動或重啟 ROS 訓練流程..."
    echo "=========================================="

    # 執行您用來「一鍵啟動所有程式」的 launch 檔
    roslaunch blender_map_project train.launch 

    # 當上面的 roslaunch 指令結束後，這裡會繼續執行
    echo "=========================================="
    echo "偵測到訓練流程已結束，將在 10 秒後自動重啟..."
    echo "=========================================="
    sleep 10 # 休息 10 秒
done
